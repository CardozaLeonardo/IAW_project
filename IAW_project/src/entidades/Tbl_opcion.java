package entidades;

public class Tbl_opcion {
	
	private int id_rol;
	private String opcion;
	private String opcion_des;
	private int estado;
	
	public int getId_rol() {
		return id_rol;
	}
	public void setId_rol(int id_rol) {
		this.id_rol = id_rol;
	}
	public String getOpcion() {
		return opcion;
	}
	public void setOpcion(String opcion) {
		this.opcion = opcion;
	}
	public String getOpcion_des() {
		return opcion_des;
	}
	public void setOpcion_des(String opcion_des) {
		this.opcion_des = opcion_des;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	
	

}
